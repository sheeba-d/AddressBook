     public void searchContact(String pattern){
      String qry = "select * from addbook where name like '"+pattern+"%' order by name asc";
        try{
          rs = st.executeQuery(qry);
            DefaultTableModel dtm =(DefaultTableModel)ctList.getModel();
            dtm.setRowCount(0);
            while(rs.next()){
                Object[] ob = {rs.getString("name").toString(),rs.getString("mob").toString(),rs.getString("email").toString()};
                dtm.addRow(ob);
            }
       
        }catch(Exception e){
          System.out.println("Load Contact: "+e);
        }
    }
