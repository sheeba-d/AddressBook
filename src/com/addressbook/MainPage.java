    private void doneImgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_doneImgMouseClicked
        // TODO add your handling code here:
        String newName = ctName1.getText();
        String newMob = ctMob1.getText();
        String newMail = ctMail1.getText();
        
        if(isNumeric(newMob)){
          try{  
           String qry = "update addbook set name='"+newName+"',mob='"+newMob+"',email='"
                +newMail+"' where name='"+pkName+"' and mob='"+pkMob+"'";
           int flag = st.executeUpdate(qry);
           if(flag>0){
            
               JOptionPane.showMessageDialog(this, "Contact Edited");
               homePanel.setVisible(false);
               addPanel.setVisible(false);
               mainPanel.setVisible(false);
               editPanel.setVisible(true);
               ctName1.setEditable(false);
               ctMob1.setEditable(false);
               ctMail1.setEditable(false);
               editImg.setVisible(true);
               delImg.setVisible(true);
               
               
           }
          }catch(Exception e){
              System.out.println("Update Error: "+e);
          }
        }else{
            JOptionPane.showMessageDialog(this,"Invalid Mobile Number");
        }
    }//GEN-LAST:event_doneImgMouseClicked

