    private void addBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtActionPerformed
        // TODO add your handling code here:
        String name = ctName.getText().trim();
        String mob = ctMob.getText();
        String mail = ctMail.getText();
        if(isNumeric(mob)){
          try{  
           String qry = "insert into addbook values('"+name+"','"+mob+"','"+mail+"')";
        
           int flag = st.executeUpdate(qry);
           if(flag>0){
            
               JOptionPane.showMessageDialog(this, "Contact Saved");
               homePanel.setVisible(false);
               addPanel.setVisible(false);
               mainPanel.setVisible(true);
               editPanel.setVisible(false);
               loadContactList();
               
           }
          }catch(Exception e){
              System.out.println("Insert Error: "+e);
          }
        }else{
            JOptionPane.showMessageDialog(this,"Invalid Mobile Number");
        }
    }//GEN-LAST:event_addBtActionPerformed

